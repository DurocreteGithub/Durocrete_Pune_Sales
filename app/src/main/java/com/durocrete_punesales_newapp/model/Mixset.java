public class Mixset {

    String grade;
    String material_type;
    String material_combination;
    String workability;
    String slump;
    String retention_period;
    String flow;
    String nature_of_work;



    public String getMaterial_type() {
        return material_type;
    }

    public void setMaterial_type(String material_type) {
        this.material_type = material_type;
    }


    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }

    public String getMaterial_combination() {
        return material_combination;
    }

    public void setMaterial_combination(String material_combination) {
        this.material_combination = material_combination;
    }

    public String getWorkability() {
        return workability;
    }

    public void setWorkability(String workability) {
        this.workability = workability;
    }

    public String getSlump() {
        return slump;
    }

    public void setSlump(String slump) {
        this.slump = slump;
    }

    public String getRetention_period() {
        return retention_period;
    }

    public void setRetention_period(String retention_period) {
        this.retention_period = retention_period;
    }

    public String getFlow() {
        return flow;
    }

    public void setFlow(String flow) {
        this.flow = flow;
    }

    public String getNature_of_work() {
        return nature_of_work;
    }

    public void setNature_of_work(String nature_of_work) {
        this.nature_of_work = nature_of_work;
    }


}
